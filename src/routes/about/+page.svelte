	<p>By now you probably already know that I am a writer. But how did I become one?</p>
	<p>
		It all began on a gloomy day in September 2012. My best friend had been blatantly ignoring my
		stellar rendition of Macklemore’s Thrift Shop because she had better things to think about.
		Like, the 300-word essay due in 10 minutes that she’d written four words for.
	</p>
	<p>
		Now, I’d seen enough gangster movies to realize that this was an opportunity to make the biggest
		deal of my life—I write her essay in exchange for her drawing my science diagrams.
	</p>
	<p>And that was the beginning of everything.</p>
	<p>
		The successful pull-off of this deal made me realize two things—one, I do enjoy writing; and
		two, I have an unhealthy addiction to deadline-induced pressure.
	</p>
	<p>So, naturally, I decided to pursue journalism.</p>
	<p>
		I one-upped decision fatigue by trying out all my choices—hard news (The Hindu, Humber News),
		investigative journalism (Surviving Hate, Humber StoryLab), technical writing (SPARK, Humber
		ORI), content writing (IGNITE), radio reporting (Radio Humber) and broadcast journalism (IGNITE,
		Humber News, Humber ORI).
	</p>
	<p>
		Currently enjoying my budding writing career, I continue to undertake every article with the
		zest of a chef crafting a Michelin-star meal. When away from my drafts, I indulge in movie
		watching, pop singing, and cat stalking.
	</p>
